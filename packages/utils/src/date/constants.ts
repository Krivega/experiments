export const secondsPerMinutes = 60;
export const minutesPerHour = 60;
export const hourPerDay = 24;
export const daysPerWeek = 7;
export const daysPerMonth = 31;
export const daysPerYear = 365;

export const msPerSecond = 1000;
export const msPerMinute = msPerSecond * secondsPerMinutes;
export const msPerHour = msPerMinute * minutesPerHour;
export const msPerDay = msPerHour * hourPerDay;
export const msPerWeek = msPerDay * daysPerWeek;
export const msPerMonth = msPerDay * daysPerMonth;
export const msPerYear = msPerDay * daysPerYear;
